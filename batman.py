from turtle import Turtle,Screen

p = Turtle()
p.up()

s = Screen()
#s.bgcolor("#aaaaff")

def BatMan(a,b):
    p.fillcolor("yellow")
    p.goto(-100,0)
    
    #make elipse
    p.down()
    p.width(10)
    p.begin_fill()
    p.right(45)
    p.circle(150,90)
    p.circle(60,90)
    p.circle(150,90)
    p.circle(60,90)
    p.end_fill()
    p.width(1)
    p.up()
    #done
    
    p.fillcolor("black")
    p.goto(5,85)
    p.down()
    p.width(2)
    q = p.clone()
    p.begin_fill()
    p.left(45)
    p.fd(5)
    p.left(60)
    p.fd(15)
    p.right(150)
    p.fd(25)
    p.circle(25,180)
    p.circle(30,60)
    p.left(5)
    p.circle(135,-20)
    p.circle(45,-90)
    p.circle(135,-10)
    p.left(5)
    p.circle(25,45)
    p.circle(10,90)
    p.circle(25,75)
    p.right(135)
    p.circle(30,10)
    p.circle(15,90)
    p.circle(45,55)
    #p.goto(5,-20)
    p.ht()
    p.end_fill()
    #done()
    
    q.begin_fill()
    q.left(225)
    q.fd(5)
    q.left(-60)
    q.fd(15)
    q.right(-150)
    q.fd(25)
    q.circle(-25,180)
    q.circle(-30,60)
    q.left(-5)

    q.circle(-135,-20)
    q.circle(-45,-90)
    q.circle(-135,-10)
    q.left(-5)
    q.circle(-25,45)
    q.circle(-10,90)
    q.circle(-25,75)
    q.right(-135)
    q.circle(-30,10)
    q.circle(-15,90)
    q.circle(-45,55)
    #q.goto(5,-20)
    q.ht()
    q.end_fill()


s.onclick(BatMan)
s.mainloop()
#done()
